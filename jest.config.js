/**
 * JEST CONFIG (ESM + TypeScript)
 * ------------------------------------------------------------
 * Назначение:
 *
 *  - Запуск юнит-тестов в Node с поддержкой ESM и TypeScript.
 *  - Резолв алиасов из tsconfig (@"/*") и «.js» суффиксов в импорт-путях (NodeNext).
 *  - Быстрый, повторяемый запуск: чистые моки, понятный поиск тестов.
 *
 * Примечания:
 *
 *  - Используется пресет ts-jest/presets/default-esm + transform с useESM.
 *  - Для Node >= 18 флаг --experimental-vm-modules в npm скрипте не нужен.
 *  - Если импортируешь JSON с assert { type: 'json' }, Jest это исполняет через ts-jest (ок).
 * ------------------------------------------------------------
 * Подробные опции: https://jestjs.io/docs/configuration
 */
const config = {
    // --------------------------------------------------------------------
    //                             ОБЩЕЕ
    // --------------------------------------------------------------------
    // Прерывать выполнение после 1 провала, чтобы не тонуть в лавине ошибок.
    bail: 1,
    // Авто-очистка моков/спаев между тестами.
    clearMocks: true,
    // Детализированный репорт по каждому тесту.
    verbose: true,
    // Корень проекта (точка отсчёта для <rootDir>).
    rootDir: '.',
    // Где искать тесты (ограничиваемся папкой tests).
    roots: ['<rootDir>/tests'],
    // Окружение исполнения — Node.
    testEnvironment: 'node',
    // Паттерны тест-файлов.
    testMatch: ['<rootDir>/tests/**/*.test.ts'],
    // Исключаем мусор из поиска тестов.
    testPathIgnorePatterns: ['/node_modules/', '/dist/'],
    // Общий таймаут для «зависших» тестов (мс).
    testTimeout: 10_000,
    // Немного тишины для вотч-режима: не реагировать на артефакты билда/кэша.
    watchPathIgnorePatterns: ['<rootDir>/dist', '<rootDir>/.tsbuildinfo'],

    // --------------------------------------------------------------------
    //                         TypeScript + ESM
    // --------------------------------------------------------------------
    // Преднастройка для ESM-режима с ts-jest.
    preset: 'ts-jest/presets/default-esm',

    // Обрабатываем .ts/.tsx через ts-jest в ESM-режиме.
    transform: {
        '^.+\\.tsx?$': [
            'ts-jest',
            {
                useESM: true,
                tsconfig: '<rootDir>/tsconfig.json',
            },
        ],
    },

    // Какие расширения считать ESM-модулями (важно для импорта .ts).
    extensionsToTreatAsEsm: ['.ts'],

    // Резолвер, понимающий tsconfig paths.
    resolver: 'ts-jest-resolver',

    // Соответствие импортов путям проекта.
    // 1) NodeNext заставляет писать суффикс ".js" в исходниках.
    //    Эти правила маппят его обратно на исходные .ts в тестах.
    // 2) Алиасы "@/..." и "tests/..." направляем в src/tests.
    moduleNameMapper: {
        // Сначала правила с .js, затем без — чтобы не перекрывать.
        '^@/(.*)\\.js$': '<rootDir>/src/$1.ts',
        '^@/(.*)$': '<rootDir>/src/$1',
        '^tests/(.*)\\.js$': '<rootDir>/tests/$1.ts',
        '^tests/(.*)$': '<rootDir>/tests/$1',
        // Разрешаем относительные импорты с ".js" к исходным .ts.
        '^(\\.{1,2}/.*)\\.js$': '$1',
    },

    // Какие расширения модулей искать.
    moduleFileExtensions: ['ts', 'tsx', 'js', 'json'],

    // --------------------------------------------------------------------
    //                           ПОКРЫТИЕ КОДА
    // --------------------------------------------------------------------
    // Включать ли сбор покрытия по умолчанию (можно включить в CI).
    collectCoverage: true,
    // Откуда собирать покрытие.
    collectCoverageFrom: [
        '<rootDir>/src/**/*.{ts,tsx}',
        '!<rootDir>/src/**/*.d.ts',
    ],
    // Что игнорировать при расчёте покрытия.
    coveragePathIgnorePatterns: ['/node_modules/', '/dist/'],
    // Провайдер покрытия на базе V8 — быстрее и ближе к реальности.
    coverageProvider: 'v8',
};

export default config;
